/**
 * @brief  Main function
 */
int main(void)
{
  // Initialize device
  initMcu();

  // Initialize board
  initBoard();

  // Initialize clocks
  	cmu_init();

  /* Initialize GPIO */
    gpio_init();

    // Initialize LETIMER0
    letimer_init();

  // Initialize stack
  gecko_init(&config);


while(1)
  {
	slp();
  }
}

